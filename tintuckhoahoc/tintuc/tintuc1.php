    <p><strong>Chùa Quan Âm</strong> (hay còn có cái tên khác là <strong>đền Long Bàn</strong>) được xây dựng lần đầu tiên từ thế kỷ 13 vào triều đại nhà Tống của Trung Quốc và đến thời nhà Nguyên thì được sửa sang, xây dựng lại.</p>

       <p> Ngôi chùa từng bị phá hủy hoàn toàn vào năm Gia Tĩnh thứ sáu của nhà Minh (1527), sau đó được huyện lệnh Hứa Sắc Khanh xây dựng lại với quy mô lớn hơn, có thêm đình Quan Âm và lầu Thuần Dương. Ngôi chùa một lần nữa được tu sửa và gia cố vào năm Đồng Trị thứ 3 của nhà Thanh (1864) và tồn tại cho tới ngày nay.</p>

       <p>  Nhờ có nền móng vững chắc, được đặt trên một hòn đảo đá ngay giữa sông Dương Tử, ngôi chùa đã sừng sững đứng hiên ngang giữa dòng sông suốt nhiều thế kỷ. Thậm chí trận lũ lịch sử 1998 và 2017 cũng không ảnh hưởng nhiều tới ngôi chùa cổ.</p>

    <img src="../hinh/tintuc1.5.jpg" height="394" width="700"/>
    <p> Ngôi chùa có chiều dài 24m, chiều rộng 10m và chiều cao 14m, gồm 2 tầng chính với nhiều cửa nhỏ được sơn màu trắng và phần mái ngói phủ rêu phong.</p>

       <p> Bà Hoàng Tuyết Cầm, chuyên viên Phòng Quản lý Văn hóa của Bảo tàng Thành phố Ngạc Châu, cho biết Quan Âm Các được xây dựng trên một phần móng vững chắc, được gọi là<strong> khối đá Long Bàn</strong>. Bên cạnh đó, phía trước ngôi chùa có một bức tường rào hình vòng cung bảo vệ.
           Hình dáng vòng cung của bức tường sẽ điều tiết dòng chảy và làm giảm lực nước, giữ cho kiến trúc phía sau bức tường chịu một trọng lực nhỏ hơn rất nhiều.</p>

        <p> Chính vì vậy mà hàng trăm năm qua, ngôi chùa vẫn đứng vững dù hứng chịu vô số trận đại hồng thủy. Đặc biệt phải kể đến trận đại hồng thủy năm 1998 hay năm 1954 trên sông Trường Giang, khoảng 30.000 người thiệt mạng. Kinh khủng hơn còn có những trận ngập lụt kỷ lục diễn ra năm 1911 cướp đi sinh mạng của khoảng 100.000 người.</p>

