        <p>Khi nước lũ ập đến, ngôi chùa sẽ bị nước tràn vào, chỉ còn lại những ô cửa sổ cao tầng lộ ra bên ngoài. Chỉ đến khi mùa khô, mực nước sông thấp thì toàn bộ ngôi chùa lại hiện ra và đứng sừng sững giữa lòng sông.</p>
        <p>Ngoài ra, chùa Quan Âm được biết tới là "ngôi chùa đầu tiên trên sông Dương Tử". Theo các chuyên gia bảo tồn di sản, các đồ đạc trong chùa đã được chuyển đi và các cửa đều được mở ra để giảm bớt tác động của dòng nước đối với chốn linh thiêng này.</p>
