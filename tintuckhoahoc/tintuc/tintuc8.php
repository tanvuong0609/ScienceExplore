        </div>
        <p>F-117 lần đầu tham gia tác chiến năm 1989. Các chuyến bay làm nhiệm vụ tác chiến tiếp theo của F-117 diễn ra khi tham gia Chiến dịch Bão táp Sa mạc, tên hiệu của cuộc tấn công lớn vào Iraq do Mỹ dẫn đầu trong những năm 1990.</p>
        <p>Bên cạnh đó, nó còn thực hiện các sứ mệnh ở Kosovo năm 1999 và Chiến dịch Tự do cho Iraq năm 2003. Mẫu máy bay này được Mỹ cho “nghỉ hưu” vào năm 2008.</p>
       <h2>3. B-2 Spirit</h2>
        <p>Trình làng năm 1988, B-2 Spirit được coi là máy bay ném bom chiến lược tàng hình chính thức đầu tiên của Mỹ. Thiết kế không có phần đuôi giúp nó giảm thiểu khả năng bị radar đối phương phát hiện.</p>
        <p>Máy bay ném bom này có thể mang mọi loại khí tài từ bom nguyên tử trọng lực B61 cho tới bom JDAM, hay bom hạng nặng GBU-57A/B (hơn 13 tấn).</p>
        <img src="tintuckhoahoc/hinh/tintuc8.2.jpg" height="386" width="700"/>
        <div class="ghichu">
            <strong>Máy bay ném bom tàng hình B-2. (Ảnh: Wiki)</strong>
        </div>
        <p>Nhiệm vụ tác chiến đầu tiên của B-2 là ở Kosovo năm 1999. B-2 cũng tham gia hỗ trợ Chiến dịch Tự do bất diệt và Tự do cho Iraq. Năm 2011, ba chiếc B-2 bay cất cánh từ lục địa Mỹ tấn công một sân bay của Libya trong chiến dịch Bình minh Odyssey.</p>
        <h2>4. F-22 Raptor</h2>
        <p>Không giống như các dòng máy bay tàng hình trước đây của Mỹ, như F-117 và B-2, F-22 Raptor được thiết kế như một máy bay chiến đấu, sử dụng kỹ năng tàng hình để đưa ra lợi thế mang tính quyết định trong các cuộc không chiến.</p>
