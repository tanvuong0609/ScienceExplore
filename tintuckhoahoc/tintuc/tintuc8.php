        <div>Tính năng cuối cùng là SR-71 được sơn đen với các hạt sắt ferrite cỡ nhỏ. Chính lớp sơn này mang lại cho SR-71 vẻ ngoài đặc biệt mà được mệnh danh là “hắc điểu”.</div>
        <div>Màu sơn này được cho là giảm thiểu mặt cắt ngang của radar. Đây là chỉ số chỉ mức độ mà radar có thể phát hiện ra vật thể. SR-71 có chỉ số này nhỏ hơn 10m2 trong khi F-15 Eagle là 100m2.</div>
       <h2>2. F-117 Nighthawk</h2>
        <p>F-117 được phát triển từ dự án tuyệt mật Have Blue với các đặc điểm tàng hình ấn tượng giúp nó hoạt động hiệu quả trong các nhiệm vụ bắn phá sở chỉ huy, trung tâm thông tin liên lạc, các hệ thống phòng thủ, kho vũ khí</p>
        <p> Nó có thiết kế khí động học rất đặc biệt với những mặt cắt cho phép làm tán xạ sóng radar, khiến máy bay trở nên khó phát hiện với các thiết bị trinh sát điện tử.</p>

        <img src="tintuckhoahoc/hinh/tintuc8.1.jpg" height="386" width="700"/>
