        <h2>1. SR-71 Blackbird</h2>
        <p>SR-71 nổi tiếng là máy bay nhanh nhất thế giới và có khả năng tàng hình ấn tượng. Nó có thể đạt tốc độ tối đa Mach 3.2 và được bổ sung 4 tính năng tàng hình vào thiết kế.</p>
        <p>Đầu tiên, bề mặt máy bay có khả năng ngăn không cho sóng radar đối phương phản xạ lại thiết bị thu sóng.</p>
        <p>Thứ hai, cánh, đuôi và thân máy bay đã được chế tạo bằng vật liệu tổng hợp cùng với titan, nhằm mục tiêu hấp thụ sóng rada</p>
