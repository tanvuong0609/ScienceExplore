  <h2>Sự thật về loài khủng long</h2>

  <p>Có thể bạn đã biết rằng, cho dù có cố phủ định thế nào chăng nữa, khủng long vẫn là những con vật nổi tiếng nhất ở thời tiền sử. To lớn, đáng sợ,... và chúng ta chẳng thể biết gì nhiều về chúng cả. Vì thế, thật không ngoa khi nói rằng chúng chính là bí ẩn lớn nhất mà ai ai cũng muốn tìm hiểu đến.</p>

  <p>Tuy nhiên, khoa học ngày càng phát triển và chúng ta vẫn ngày càng khám phá thêm được nhiều điều hơn về chúng. Vậy, hãy cùng tìm hiểu về top 10 điều kỳ thú về những sinh vật này nhé.</p>

  <h2>1. Hồi đầu tiên, người ta từng nghĩ khủng long là... voi?</h2>

  <p>Vào năm 1677, giáo sư Robert Plot người Anh đã tìm thấy 1 chiếc xương khổng lồ được vùi trong đất, tại 1 mỏ đá tại Oxford. Ở thời điểm đó, các nhà khoa học khác đều rất bối rối, và sau đấy họ liền kết luận vội vàng rằng đó chắc hẳn là xương của... 1 con voi kích cỡ khủng. Phải, ở thời đó thì chẳng có ai tưởng tượng rằng có những loài động vật to lớn như khủng long lại từng tồn tại trên Trái đất này, đó là lý do họ nghĩ đến loài động vật trên cạn có kích cỡ lớn nhất hiện tại.</p>

  <p>Dần dần, các nhà khoa học mới khám phá thêm được nhiều điều mới. Năm 1842, nhà động vật học William Buckland đã gây ra những phản ứng mạnh khi khẳng định rằng những chiếc xương này là của 1 loài bò sát khổng lồ đã tuyệt chủng, thông qua việc tìm thấy nhiều phần xương khác tương đồng với thứ mà Robert tìm được khi xưa. Ông gọi đó là "khủng long".</p>

  <h2>2. Khủng long có màu như thế nào?</h2>

