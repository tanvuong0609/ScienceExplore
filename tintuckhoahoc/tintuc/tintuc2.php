  <p>Bạn từng nghĩ về vấn đề này nhiều lần rồi phải không? Dĩ nhiên là chúng ta không hề biết vì các hóa thạch chỉ cung cấp những thông tin về bộ xương, chứ chẳng để lại những thông tin nào để chúng ta biết về màu da cả. Tất cả những gì bạn thấy trên phim cũng đều chỉ là phỏng đoán mà thôi.</p>

  <p>Đối với nhiều người, khủng long có thể có rất nhiều màu da khác nhau. Nếu các loài bò sát hiện nay có nhiều loài sở hữu nước da màu xanh, thì tại sao khủng long lại không thể nhỉ? Hoặc cũng có thể chúng sẽ có những phần phụ nhiều màu trên cơ thể, ví dụ như 1 phần mào có màu sáng để quyến rũ con cái trong mùa sinh sản chẳng hạn.</p>

  <h2>3. Làm thế nào để phân biệt khủng long đực và khủng long cái?</h2>

  <p>Về cơ bản thì đây đúng là câu hỏi thế kỷ và rất khó có lời giải đáp. Cho đến giờ, tất cả những gì chúng ta làm chỉ có thể là phỏng đoán mà thôi, bởi khủng long chẳng để lại cái gì cho chúng ta ngoài mấy bộ xương không lành lặn và 1 đống trứng hóa thạch cả.

    Ở thời điểm khai quật, các nhà khoa học thường xuyên mò thấy những bộ xương khủng long có kích cỡ khác nhau nằm cạnh nhau, và lâu dần thì họ đi đến phỏng đoán rằng: những con đực và con cái của 1 loài sẽ không có cùng tầm vóc. Theo các nhà khoa học, khủng long T-REX cái sẽ có kích thước lớn hơn con đực</p>
  <h2>4. Khủng long... không phải là sinh vật thông minh gì cho lắm</h2>
  <p> Mặc dù có cơ thể tương đối khổng lồ, nhưng khủng long vốn chẳng thông minh gì cho cam vì chúng có một bộ não bé tẹo. Ví dụ, mặc dù nặng từ 2 đến 3 tấn, nhưng khủng long Stegosaurus chỉ có 1 bộ não nhỏ cỡ quả hồ đào mà thôi.

    Còn nếu nói về con thuộc hàng thông minh nhất? Chắc hẳn là phải nhắc đến Troodon rồi, vì não chúng to bằng quả trứng gà đó. Có lẽ, đây là lý do mà Troodon được mô tả là tương đối năng động, tinh quái với vị thế kẻ ăn thịt, còn Stegosaurus thì được coi như 1 loài khủng long có khả năng phản ứng tương đối chậm?</p>


