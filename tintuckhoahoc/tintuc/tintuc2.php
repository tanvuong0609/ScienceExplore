    <p>Theo những hóa thạch mà các nhà khoa học tìm được, khủng long đẻ trứng và đặt trứng thành vòng tròn đều đặn trong 1 cái tổ đào sâu trong đất. Nhưng hồi đầu, các nhà khoa học lại cho rằng chúng chỉ đẻ trứng, chứ không chăm sóc trứng giống như đa số các loài bò sát. Nhưng cuối cùng, họ đã nhận ra rằng khủng long có hiện tượng ấp trứng, khi họ tìm thấy những bộ xương khủng long phủ lên tổ của chúng.</p>
    <h2>8. Tiền thân của khủng long là những sinh vật rất nhỏ bé so với chúng?</h2>
    <p>Theo nghiên cứu, tổ tiên của khủng long - loài Lagosuchus thuộc nhóm bò sát nguyên thủy Archosaur chỉ có kích thước cỡ 1 con thỏ: dài 50cm, nặng 100 gram, có chân trước ngắn hơn chân sau giống như những loài khủng long sau này.

      Loài khủng long cổ xưa nổi tiếng nhất được cho là khủng long Eoraptor (nghĩa là "kẻ cướp bình minh"), sống vào kỷ Trias cách đây khoảng 225 triệu năm, được khám phá vào năm 1991 tại Argentina. Nó chỉ đạt chiều dài có 1m và chỉ nặng 11kg.

      Sau này, các nhà khoa học cũng tìm được nhiều bộ xương khác có kích cỡ còn nhỏ hơn thế. Nhỏ nhất có lẽ là loài Microraptor, khi chúng chỉ dài có... 40cm và to nhất được biết tới nay là 83cm, trong khi loài to nhất: Amphicoelias lại dài tới 60m.</p>

   <h2>9. Một số loài khủng long có khả năng điều hòa thân nhiệt?</h2>

    <img src="../hinh/tintuc2.4.jpg" height="402" width="640"/>
    <p>Thông thường thì đáng ra chuyện đó là bất khả thi bởi khủng long vốn thuộc loài bò sát. Điều này có nghĩa rằng chúng đều có máu lạnh giống như thằn lằn, và phải sưởi ấm dưới ánh mặt trời. Nhưng đối với khủng long Sauropod, vì chúng quá to lớn nên người ta cho rằng nếu chúng tích nhiệt từ Mặt trời thì sẽ không thể có thời gian giải phóng lượng nhiệt này đi. Chính vì thế, việc chúng có khả năng điều hòa thân nhiệt rõ ràng là giả thuyết khả thi nhất.

      Bên cạnh đó, theo kết quả nghiên cứu từ mặt cắt xương của 1 số loài khủng long ăn thịt nhỏ, các nhà khoa học đã nhận ra rằng nếu có những loài khủng long có máu lạnh, thì cũng có những loài khác phải có máu nóng. Đây chính là đặc điểm của chim - những loài được cho là hậu duệ của khủng long.</p>
    <h2>10. Lớn lên thì to, còn khi mới nở thì bé xíu</h2>
    <p>Kể cả đối với những loài khủng long có kích cỡ thuộc hàng to lớn nhất khi trưởng thành thì cũng có những con bé xíu, khác hẳn trong tưởng tượng của con người. Có những chú khủng long sơ sinh chỉ nặng dưới 3kg và dài 40-5 cm mà thôi. Sở dĩ khủng long con bé như vậy là vì chúng ra đời trong những quả trứng dài trung bình chỉ 45cm, còn những quả to nhất mà các nhà khoa học tìm được cũng chỉ dài cỡ 60cm.

      Dĩ nhiên, trong trường hợp đó thì trứng khủng long không thể to hơn được. Việc tăng kích thước sẽ dẫn đến việc khủng long con không thể chọc thủng vỏ trứng lúc nở, nghĩa là dễ khiến chúng bị kẹt cứng ở bên trong.

      Ngược lại. khủng long con lại lớn nhanh như thổi, do tốc độ phát triển cần phải nhanh để đạt được đủ sức mạnh để tự vệ, chống lại những kẻ săn mồi nguy hiểm khác.</p>

