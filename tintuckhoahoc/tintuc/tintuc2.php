  <h2>5. Vì sao chúng nhanh chóng trở thành loài thống trị Trái đất?</h2>
  <p>Ở thời đầu đại Trung sinh cách đây 250 triệu năm, 90% các loài đã biến mất. Chính điều này đã mang lại cho khủng long một môi trường tự do để phát triển. Khi cuộc hủy diệt thứ 2 bất ngờ xảy ra vào cuối kỉ Trias cách đây 210 triệu năm, khủng long đã không còn đối thủ và nhanh chóng trở thành loài thống trị.</p>
  <h2>6. Vì sao có loài khủng long đi bằng 2 chân, còn những loài khác thì đi bằng 4 chân?</h2>
  <p>Một trong những đặc điểm chung của khủng long là 2 chân trước luôn ngắn hơn 2 chân sau. Như vậy, tất cả khủng long đều có thể đi thẳng bằng 2 chân sau theo lý thuyết. Tuy nhiên, tại sao đa số những loài khủng long ăn thực vật thì không?</p>
  <p>Câu trả lời ở đây chính là yếu tố <strong>"cân nặng"</strong> . Về mặt cơ bản, việc sở hữu cơ thể phát triển đến mức trở nên quá to và nặng nề đã khiến chúng buộc phải quay lại với việc đi bằng 4 chân, chứ không thể đứng lên bằng 2 chân được.</p>
  <h2>7. Khủng long chăm sóc trứng như thế nào?</h2>

