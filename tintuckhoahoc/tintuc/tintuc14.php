    <p><strong>Hoạt động trên không trung</strong>
      <p> Thực tế, máy bay có thể bay ở độ cao bao nhiêu tùy ý thích. Kỷ lục thế giới từng ghi nhận trường hợp máy bay SR – 71 Blackbird của Mỹ đạt tới độ cao gần 26km so với mặt nước biển năm 1976. Còn máy bay thương mại dân dụng thông thường thì bay ở độ cao 8,5km - 10,7km, tức là nằm trong phần trên tầng đối lưu của khí quyển.</p>
   </p> <img src="tintuckhoahoc/hinh/tintuc13.2.jpg" height="450" width="600"/>
    <p>Nhiệt độ giảm dần khi lên cao, mức thấp nhất là khoảng -50 độ C. Do đó, dù ở trong khoang chứa bánh xe của máy bay cũng khó mà ngăn được khí lạnh do độ dày vật liệu cách nhiệt cũng như hệ thống sưởi không có như trong khoang chở hành khách. Cùng với đó, không khí trong tầng đối lưu chuyển động theo chiều thẳng đứng và nằm ngang rất mạnh, gây ra rất nhiều hiện tượng mưa, mưa đá, gió, tuyết, sương mù…
    </p>

     <p>Tốc độ bay ổn định của máy bay dân dụng khoảng 900km/h. Càng lên cao, sức cản không khí càng giảm đi (trên 5,5km sức cản giảm một nửa), giúp máy bay bay nhanh hơn. Đồng thời, bay cao giúp máy bay hạn chế nguy cơ đâm phải những đàn chim di cư - một trong những nguyên nhân gây nên những tai nạn thảm khốc trên bầu trời.</p>
  </p>

    <img src="tintuckhoahoc/hinh/tintuc13.3.jpg" height="375" width="600"/>
    <p><strong> Áp suất không khí </strong> trong máy bay khi lên cao giảm đi rất nhiều, chỉ khoảng 1/10 so với trên mặt đất.</p>

     <p> Nhưng ngược lại, không khí càng lên cao càng loãng. Áp suất không khí cũng giảm đi rất nhiều, chỉ còn khoảng 1/10 so với trên mặt đất. Do đó, lượng không khí có ở trong khoang chứa bánh xe là rất thấp.


     <p> Đối với những máy bay dùng động cơ cánh quạt, lượng oxy thấp đi cũng khiến việc đốt nguyên liệu trở nên khó khăn. Trên không, các máy bay cũng thường thu bánh xe lại bởi nếu giữ nguyên, không khí sẽ lùa vào bánh, làm nó chuyển động xoáy tròn, gây sức cản làm giảm tốc độ bay.
  </p>
    <p> <strong> Cơ chế hạ cánh của máy bay</strong></p>

     <p> Chuyến bay kết thúc với sự hạ cánh của máy bay trên đường băng. Khi máy bay gần tiếp đất, không khí phía dưới cánh bị ép xuống mặt đất, gây nên những luồng xoáy nhỏ cũng như tạo “hiệu ứng mặt đất” (ground effect) làm máy bay nâng lên trong một thời gian ngắn trước khi chính thức tiếp xuống đường băng.</p>
</p>


    <img src="tintuckhoahoc/hinh/tintuc13.4.jpg" height="349" width="600"/>

