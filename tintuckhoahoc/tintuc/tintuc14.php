    <p><strong>Cơ chế cất cánh của máy bay</strong>
      <br> Máy bay có trọng lượng tới cả trăm tấn. Nhiều người sẽ tự hỏi vậy thứ lực khủng khiếp nào có thể nhấc “con quái vật không trung” ấy lên khỏi mặt đất? Câu trả lời chính là lực nâng khí động lực học (còn gọi là lực nâng Joukowski).</p>
    <img src="tintuckhoahoc/hinh/tintuc13.jpg" height="300" width="600"/>

  <p>Cụ thể, một chiếc máy bay chuyển động bao giờ cũng chịu tác dụng của 4 lực: lực kéo, lực cản của không khí, lực hấp dẫn và lực nâng. Theo đó, khi máy bay chạy trên đường băng, dòng khí chảy bao quanh cánh máy bay sẽ tạo ra sự chênh lệch áp suất giữa mặt dưới cánh và mặt trên cánh.</p>
