        <p>Người đàn ông 77 tuổi khẳng định NASA đã liên lạc với những người ngoài hành tinh, miêu tả họ <i>"là những con người bé nhỏ nhìn chúng tôi một cách lạ lẫm"</i>.</p>
        <p>Theo miêu tả của ông Mitchell, người ngoài hành tinh có dáng người bé nhỏ, mắt và đầu to.</p>
        <p>Ông Mitchell nói rằng người ngoài hành tinh có dáng dấp đúng như những gì mà các nhà sản xuất phim tưởng tượng: thân hình bé nhỏ, mắt và đầu to.</p>
        <p>“Không có gì nghi ngờ khi nói rằng cuộc sống tồn tại trên khắp vũ trụ. Chúng ta không đơn độc. Tôi có thể khẳng định điều đó”.</p>
        <p>“Liệu chúng ta có thể xác định những hành tinh đó ở đâu? Không, tất nhiên là không trong Hệ mặt trời. Nhưng chúng ta có thể xác định một số hành tinh tồn tại sự sống”.</p>
        <p>Theo lời Mitchell, công nghệ của con người không tinh vi bằng công nghệ của người ngoài hành tinh, và rằng con người có thể không còn trên trái đất nếu bị người ngoài hành tinh căm ghét. “Họ không có hại và không thù địch với con người”.</p>
        <p>Ông Mitchell, cùng với chỉ huy tàu Apollo 14 Alan Shepard, hiện đang giữ kỷ lục cuộc đi bộ dài nhất trên mặt trăng với thời gian 9 giờ, 17 phút vào năm 1971.</p>
        <p>Trong cuộc phỏng vấn, ông nói: “Người ngoài hành tinh đã ghé thăm trái đất và hiện tượng UFO là có thật... Chuyện đó đã bị giấu kín bởi tất cả các chính phủ trong 60 năm qua, nhưng dần dần nó đã bị rò rỉ”.</p>
        <p>“Tôi nằm trong số các quan chức bình báo và quân đội biết sự thật đằng sau những gì công chúng biết, đó là trái đất đã được ngoài hành tinh ghé thăm. Đọc báo thời gian gần đây, tôi thấy chuyện đó đã xảy ra khá nhiều”.</p>
        <p>“Đây mới là sự khởi đầu trong quá trình công khai mọi chuyện. Tôi nghĩ chúng ta đang tiến tới việc mở tấm rèm bí mật và một số tổ chức nghiêm túc đang hướng tới điều này”. </p>
        <p>Mitchell có bằng kỹ sư hàng không và là tiến sĩ khoa học về hàng không và du hành vũ trụ. Ông khẳng định đĩa bay Roswell "đổ bộ" trái đất năm 1947 là có thật và những chuyến viếng thăm tương tự của người ngoài hành tinh sẽ tiếp tục được điều tra.</p>
        <p>Cơ quan hàng không vũ trụ Mỹ NASA đã ra tuyên bố nói rằng họ không đồng tình với những bình luận của Mitchell.</p>
