        <img src="tintuckhoahoc/hinh/tintuc7.jpg" height="386" width="700"/>
    <p>Theo trang Interesting Engineering, nhóm nghiên cứu đến từ Viện Khoa học vật lý Hợp Phì (HFIPS) ở Trung Quốc đã tạo ra đèn plasma bền nhất thế giới.</p>
    <p><strong>Đèn plasma hay đèn khò plasma </strong>là một thiết bị tạo ra dòng plasma liên tục bằng cách cung cấp năng lượng đủ lớn vào một loại khí, tách các electron ra khỏi nguyên tử và phân tử.</p>
    <p>Đèn plasma thường sử dụng khí trơ như heli, neon và argon - hydro. Trong đó, hỗn hợp khí argon - hydro là loại phổ biến nhất vì giúp tiết kiệm chi phí, tạo ra dòng plasma nóng nhất và vết cắt rõ ràng nhất.</p>
        <p>Loại đèn này thường được sử dụng trong ngành công nghiệp nặng, chẳng hạn như cắt kim loại.</p>
  <p>Tuy nhiên, “tuổi thọ” của đèn plasma rất thấp, chỉ kéo dài khoảng 160 giờ (gần 7 ngày). Điều này đồng nghĩa với việc chi phí sửa chữa và bảo trì thiết bị sẽ tăng lên rất nhiều.</p>
        <p>Nhóm nghiên cứu đến từ HFIPS do giáo sư Zhao Peng dẫn đầu đã tìm ra giải pháp đơn giản và hiệu quả để xử lý vấn đề này.</p>
        <p>Dưới sự hỗ trợ của Viện hàn lâm Khoa học Trung Quốc, nhóm của ông Peng đã phát triển một hệ thống cấp điện liên tục cho đèn plasma, giúp ngọn đèn hoạt động trong nhiều năm.</p>
        <p>Ông LI Jun, kỹ sư cao cấp tại HFIPS kiêm thành viên của nhóm nghiên cứu, cho biết: <strong>“Thiết kế (của hệ thống cấp điện) này giúp vượt qua 5 rào cản chính, bao gồm độ dẫn điện, độ dẫn nhiệt, độ kín, làm mát bằng nước và cơ chế đẩy liên tục”.</strong></p>
<p>Với phát minh này, đèn plasma sẽ không cần “nghỉ ngơi” sau một khoảng thời gian ngắn hoạt động. Từ đó giúp nhiều công ty, doanh nghiệp tiết kiệm chi phí vận hành, bảo dưỡng.</p>
        <storng>“Chúng tôi đã tạo ra đèn plasma có tuổi thọ cao nhất thế giới”, giáo sư Peng nêu trong thông cáo báo chí.</storng>
        <p>Theo trang Interesting Engineering, việc "nâng cấp" đèn plasma nói trên không chỉ giúp nâng cao hiệu quả hoạt động của con người mà còn góp phần phát triển nhiều ứng dụng liên quan đến các ngành công nghiệp.</p>




